void setupGPIO()
{
	*CMU_HFPERCLKEN0 |= CMU2_HFPERCLKEN0_GPIO;
	*GPIO_PC_MODEL  = 0x33333333;
	*GPIO_PC_DOUT   = 0xFF;
	*GPIO_EXTIPSELL = 0x22222222;
	*GPIO_EXTIFALL  = 0xFF;
	*GPIO_EXTIRISE  = 0xFF;
	*GPIO_IEN       = 0xFF;
}

void buttonRoutine()
{
	switch(*GPIO_PC_DIN & 0xFF)
	{
		case 0b11111110:
			soundPlay(lisa[0], lisa[1], lisa[2], 0);
			break;

		case 0b11111101:
			soundPlay(game[0], game[1], game[2], 0);
			break;

		case 0b11111011:
		soundPlay(heli[0], heli[1], heli[2], 0);
			break;

		case 0b11110111:
		soundPlay(victory[0], victory[1], victory[2], 0);
			break;

		case 0b11101111:
		soundPlay(fail[0], fail[1], fail[2], 0);
			break;

		case 0b10111111:
			soundStop();
			break;
	}
}